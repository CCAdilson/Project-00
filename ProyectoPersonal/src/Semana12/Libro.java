package Semana12;
public class Libro {
    
    private String titulo;
    private String autor;
    public int ISBN;
    private double precio;
    
    public String getTitulo(){
        return titulo;
    }
    
    public void setTitulo(String titulo){
        this.titulo = titulo;
    }
    
    public String getAutor(){
        return autor;
    }
    
    public void setAutor(String autor){
        this.autor = autor;
    }
    
    public double getPrecio(){
        return precio;
    }
    
    
    
}
